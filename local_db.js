    static getStats() {
        const tasks = this.getTasks();
        const spendings = this.getSpendings();
        
        let calcBudget = 0;
        tasks.forEach(t => {
            if (t.stt.split('.').length === 1 && t.budget) {
                calcBudget += t.budget.ngan_sach_tong || 0;
            }
        });
        const finalBudget = calcBudget || 320000;
        
        const approvedSpendings = spendings.filter(s => s.trang_thai_duyet === 'Approved');
        const totalSpent = approvedSpendings.reduce((sum, s) => sum + s.so_tien_chi, 0);
        
        const lvl1Tasks = tasks.filter(t => t.stt.split('.').length === 1);
        const avgProgress = lvl1Tasks.length ? (lvl1Tasks.reduce((sum, t) => sum + t.tien_do, 0) / lvl1Tasks.length) : 0;
        
        const rolledUpDeadlines = this.computeRolledUpDeadlines(tasks);
        let todo = 0, inprogress = 0, done = 0, delayed = 0;
        
        tasks.forEach(t => {
            const parts = t.stt.split('.');
            const tLevel = parts.length;
            let status = t.trang_thai;
            if (t.phase_id === 1 || t.phase_id === 3 || t.phase_id === 4) {
                status = this.calculatePhase134Status(t, rolledUpDeadlines);
            } else if (t.phase_id === 2 && tLevel === 3) {
                status = this.calculatePhase2Status(t, rolledUpDeadlines);
            }
            
            if (status === 'Todo') todo++;
            else if (status === 'In-Progress') inprogress++;
            else if (status === 'Done') done++;
            else if (status === 'Delayed') delayed++;
        });
        
        const lockedCount = tasks.filter(t => t.budget && t.budget.is_locked).length;
        
        return {
            tong_ngan_sach: finalBudget,
            tong_thuc_chi: totalSpent,
            con_lai: finalBudget - totalSpent,
            tien_do_trung_binh: avgProgress,
            trang_thai_tasks: {
                Todo: todo,
                "In-Progress": inprogress,
                Done: done,
                Delayed: delayed
            },
            so_wbs_bi_khoa: lockedCount
        };
    }

