    static parseDate(dateStr) {
        if (!dateStr) return null;
        const clean = dateStr.trim();
        if (/^\d{4}-\d{2}-\d{2}$/.test(clean)) {
            return new Date(clean);
        }
        const match = clean.match(/^(?:Tháng|tháng|Th\s*)\s*(\d{2})\/(\d{4})$/i);
        if (match) {
            const month = parseInt(match[1]);
            const year = parseInt(match[2]);
            return new Date(year, month, 0);
        }
        return null;
    }

    static computeRolledUpDeadlines(tasks) {
        const rolled = {};
        tasks.forEach(t => {
            let deadlineDate = this.parseDate(t.thoi_han_hoan_thanh);
            if (deadlineDate) {
                const parts = t.stt.split('.');
                for (let i = parts.length; i > 0; i--) {
                    const parentStt = parts.slice(0, i).join('.');
                    const currentRolled = rolled[parentStt];
                    if (!currentRolled || deadlineDate > currentRolled) {
                        rolled[parentStt] = deadlineDate;
                    }
                }
            }
        });
        return rolled;
    }

    static calculatePhase2Status(task, rolledUpDeadlines) {
        if (task.tien_do >= 100 || task.trang_thai === 'Done') {
            return 'Done';
        }
        const currentDate = new Date('2026-07-07');
        const compDate = this.parseDate(task.thoi_han_hoan_thanh);
        if (!compDate) return task.trang_thai;
        
        let tkbvtcDate = new Date('2026-06-30');
        if (currentDate < tkbvtcDate) {
            return 'Todo';
        }
        const delayLimit = new Date(compDate.getTime() - 20 * 24 * 60 * 60 * 1000);
        if (currentDate > delayLimit) {
            return 'Delayed';
        }
        if (tkbvtcDate <= currentDate && currentDate <= compDate) {
            return 'In-Progress';
        }
        return task.trang_thai;
    }

    static calculatePhase134Status(task, rolledUpDeadlines) {
        if (task.tien_do >= 100 || task.trang_thai === 'Done') {
            return 'Done';
        }
        const rolledDate = rolledUpDeadlines[task.stt] || this.parseDate(task.thoi_han_hoan_thanh);
        if (!rolledDate) return task.trang_thai;
        
        const currentDate = new Date('2026-07-07');
        if (currentDate < rolledDate) {
            return 'In-Progress';
        } else {
            return 'Delayed';
        }
    }

    static recalculateBudgets(tasks) {
        const allStts = new Set(tasks.map(t => t.stt));
        const parentTasks = tasks.filter(t => {
            return tasks.some(other => other.stt.startsWith(t.stt + '.'));
        });
        
        parentTasks.sort((a, b) => b.stt.split('.').length - a.stt.split('.').length);
        
        parentTasks.forEach(parent => {
            const parentLen = parent.stt.split('.').length;
            const children = tasks.filter(child => {
                const childParts = child.stt.split('.');
                return child.stt.startsWith(parent.stt + '.') && childParts.length === parentLen + 1;
            });
            
            let total = 0;
            children.forEach(child => {
                total += (child.budget ? child.budget.ngan_sach_tong : 0) || 0;
            });
            
            if (!parent.budget) {
                parent.budget = { ngan_sach_tong: 0, is_locked: false };
            }
            parent.budget.ngan_sach_tong = total;
        });
    }

